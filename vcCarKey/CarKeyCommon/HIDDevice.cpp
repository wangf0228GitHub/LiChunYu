void CHIDDevice::DECRYPT()
{
	int CNT0 = 0;
	byte j = 5;
	byte i;
	byte MASK;
	byte tmp_K;
	tmp_K = 0;
	while (CNT0 < 528) //0x210
	{
		if ((CNT0 & 0x0007) == 0)
		{
			if (j == 7)
			{
				j = 0;
			}
			else
			{
				j++;
			}
			tmp_K = KEY[j];
		}

		if ((HOP[1] & 0x08) == 0x08)
		{
			MASK = 0x10;
		}
		else
		{
			MASK = 0x01;
		}
		if ((HOP[2] & 0x01) == 0x01)
		{
			MASK = (byte)(MASK << 2);
		}
		if ((HOP[3] & 0x01) == 0x01)
		{
			MASK = (byte)(MASK << 1);
		}

		if ((HOP[0] & 0x02) == 0x02)
		{
			i = 0x01;
		}
		else
		{
			i = 0x00;
		}

		if ((HOP[0] & 0x40) == 0x40)
		{
			i = (byte)(i + 0x02);
		}

		if ((MASK & B[i]) == 0)
		{
			MASK = 0x00;
		}
		else
		{
			MASK = 0x80;
		}
		MASK = (byte)(MASK ^ HOP[2] ^ HOP[0] ^ tmp_K);

		HOP[0] = (byte)(HOP[0] << 1);
		if ((HOP[1] & 0x80) == 0x80)
		{
			HOP[0] = (byte)(HOP[0] | 0x01);
		}
		else
		{
			HOP[0] = (byte)(HOP[0] & 0xFE);
		}

		HOP[1] = (byte)(HOP[1] << 1);
		if ((HOP[2] & 0x80) == 0x80)
		{
			HOP[1] = (byte)(HOP[1] | 0x01);
		}
		else
		{
			HOP[1] = (byte)(HOP[1] & 0xFE);
		}

		HOP[2] = (byte)(HOP[2] << 1);
		if ((HOP[3] & 0x80) == 0x80)
		{
			HOP[2] = (byte)(HOP[2] | 0x01);
		}
		else
		{
			HOP[2] = (byte)(HOP[2] & 0xFE);
		}

		HOP[3] = (byte)(HOP[3] << 1);
		if ((MASK & 0x80) == 0x80)
		{
			HOP[3] = (byte)(HOP[3] | 0x01);
		}
		else
		{
			HOP[3] = (byte)(HOP[3] & 0xFE);
		}

		tmp_K = (byte)(tmp_K << 1);
		CNT0++;
	}
}

