	CString strTemp,strTemp1,strInfo;
	char ljh[11];
	for (int i = 0; i < 10; i++)
		ljh[i] = pBuf[0x4 + i];
	ljh[10] = 0;
	str=ljh;
	strInfo=_T("");
	strTemp.LoadString(IDS_MBLJH);
	strTemp=_T("EZS ")+strTemp;
	strInfo+=strTemp+_T(" : ")+str;
	strInfo+="\r\n";


	strTemp.LoadString(IDS_HardStatus);
